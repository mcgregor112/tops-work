# q - Write a Python program to import the math module and use functions like sqrt(), ceil(), floor().

# Answer :

import math

num = int(input)
sqrt_result = math.sqrt(num)
print(f"The square root of {num} is: {sqrt_result}")

num2 = 4.3
ceil_result = math.ceil(num2)
print(f"The ceiling value of {num2} is: {ceil_result}")

num3 = 4.7
floor_result = math.floor(num3)
print(f"The floor value of {num3} is: {floor_result}")





