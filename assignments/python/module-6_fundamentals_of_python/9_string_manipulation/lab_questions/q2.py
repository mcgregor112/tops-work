# q - Write a Python program that manipulates and prints strings using various string methods.

# Answer:

name = input("Enter your name:")
print(name.lower())
print(name.upper())
print(name.capitalize())
print(name.title())
print(name.swapcase())
print(name.startswith("py"))
print(name.endswith("on"))
print(name.find("th"))
print(name.replace("th", "Th"))
print(name.count("t"))
print(name.split(" "))
print(name.split("p"))





